# zadanie  z systemami liczbowymi
# 777 (ósemkowo)
# 1011 (binarnie)
# FFF (szesnastkowo)
# 123 (piątkowo)

print("Liczba 777 w systemie ósemkowym to: ", 0o777, ",a wyliczona ręcznie to:", 7 * 8 ** 0 + 7 * 8 ** 1 + 7 * 8 ** 2)
print("Liczba 1011 w systemie binarnym to: ", 0b1011, ",a wyliczona ręcznie to:", 1 * 2 ** 0 + 1 * 2 ** 1 + 1 * 2 ** 3)
print("Liczba FFF w systemie szesnastkowym to: ", 0xFFF, ",a wyliczona ręcznie to:",
      15 * 16 ** 0 + 15 * 16 ** 1 + 15 * 16 ** 2)
print("Liczba 123 w systemie piątkoym wyliczona ręcznie to: ", 3 * 5 ** 0 + 2 * 5 ** 1 + 5 ** 2)
